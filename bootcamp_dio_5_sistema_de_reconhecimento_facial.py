# Carregando o modelo pr√©-treinado

base_model = MobileNetV2(weights="imagenet", include_top=False, input_shape=(224, 224, 3))

# Adicionar camadas personalizadas para reconhecimento facial
x = base_model.output
x = GlobalAveragePooling2D()(x)
x = Dense(128, activation="relu")(x)
x = Dense(64, activation="relu")(x)
predictions = Dense(2, activation="softmax")(x)  # Altere para o n√∫mero de classes desejado

model = Model(inputs=base_model.input, outputs=predictions)

# Congelar as camadas do modelo base
for layer in base_model.layers:
    layer.trainable = False

# Compilar o modelo
model.compile(optimizer="adam", loss="categorical_crossentropy", metrics=["accuracy"])

print("‚úÖ Modelo carregado e pronto para Fine-Tuning!")

